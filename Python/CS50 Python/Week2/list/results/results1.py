results =["Mario", "Luigi", "Peach", "Toad", "Yoshi"]

results.remove("Toad") # This will remove the first occurrence of "Toad"
results.insert(2, "Bowser") # This will insert "Bowser" at index 2
results.reverse() # This will reverse the list
results.sort() # This will sort the list in ascending order
results.pop() # This will remove and return the last element of the list
results.clear() # This will remove all elements from the list
print(results)